// Functions

function add(a, b) {
    return a + b;
}

function subtract(a,b) {
    return a - b;
}

function multiply(a,b) {
    return a * b;
}

function divide(a,b) {
    return a / b;
}

console.log(`Add => ${add(1,2)}`);
console.log(`Subtract => ${add(3,4)}`);
console.log(`Multiply => ${add(5,6)}`);
console.log(`Divide => ${add(7,8)}`);